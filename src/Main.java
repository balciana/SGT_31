public class Main {
public static void main(String[] args) {
    System.out.println("Hello World!");
    //declaration of variable x
    int x;

    //initialization of variable x
    x = 15;

    /*
    this is multi line comment
     */
    System.out.println(x);

    //initialization and declaration at the same line
    int y = 10;
    System.out.println("y = " + y);

    //string data type
    String name = "Ana";
    System.out.println(name);

    //double data type
    double temperature = 36.6d + 1;
    System.out.println(temperature);

    String temperature2 = "36.6" + 1;
    System.out.println(temperature2);

    //final variable
    final double pie;
    pie = 3.1415344354323d;

}
}


